package fsp;

public class YSpec2FSPException extends Exception {

	public YSpec2FSPException() {
	}

	public YSpec2FSPException(String arg0) {
		super(arg0);
	}

	public YSpec2FSPException(Throwable arg0) {
		super(arg0);
	}

	public YSpec2FSPException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
